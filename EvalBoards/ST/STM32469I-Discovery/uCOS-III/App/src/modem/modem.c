/*
 * PPP status callback
 * ===================
 *
 * PPP status callback is called on PPP status change (up, down, ...) from lwIP
 * core thread
 */

/* PPP status callback example */
static void pppos_status_cb(ppp_pcb *pcb, int err_code, void *ctx)
{
    struct netif *pppif = ppp_netif(pcb);
    LWIP_UNUSED_ARG(ctx);

    switch(err_code)
    {
        case PPPERR_NONE:
        {
#if LWIP_DNS
            const ip_addr_t *ns;
#endif /* LWIP_DNS */
            printf("ppp_cb: Connected\n");
#if PPP_IPV4_SUPPORT
            printf("   ip_addr     = %s\n", ipaddr_ntoa(&pppif->ip_addr));
            printf("   gw          = %s\n", ipaddr_ntoa(&pppif->gw));
            printf("   netmask     = %s\n", ipaddr_ntoa(&pppif->netmask));
#if LWIP_DNS
            ns = dns_getserver(0);
            printf("   dns1        = %s\n", ipaddr_ntoa(ns));
            ns = dns_getserver(1);
            printf("   dns2        = %s\n", ipaddr_ntoa(ns));
#endif /* LWIP_DNS */
#endif /* PPP_IPV4_SUPPORT */
#if PPP_IPV6_SUPPORT
            printf("   our6_ipaddr = %s\n", ip6addr_ntoa(netif_ip6_addr(pppif, 0)));
#endif /* PPP_IPV6_SUPPORT */

            /*Set PPP interface as the default network interface*/
            ppp_set_default(pcb);  
            break;
        }
        case PPPERR_PARAM:
        {
            printf("ppp_cb: Invalid parameter\n");
            break;
        }
        case PPPERR_OPEN:
        {
            printf("ppp_cb: Unable to open PPP session\n");
            break;
        }
        case PPPERR_DEVICE:
        {
            printf("ppp_cb: Invalid I/O device for PPP\n");
            break;
        }
        case PPPERR_ALLOC:
        {
            printf("ppp_cb: Unable to allocate resources\n");
            break;
        }
        case PPPERR_USER:
        {
            printf("ppp_cb: User interrupt\n");
            break;
        }
        case PPPERR_CONNECT:
        {
            printf("ppp_cb: Connection lost\n");
            break;
        }
        case PPPERR_AUTHFAIL:
        {
            printf("ppp_cb: Failed authentication challenge\n");
            break;
        }
        case PPPERR_PROTOCOL:
        {
            printf("ppp_cb: Failed to meet protocol\n");
            break;
        }
        case PPPERR_PEERDEAD:
        {
            printf("ppp_cb: Connection timeout\n");
            break;
        }
        case PPPERR_IDLETIMEOUT:
        {
            printf("ppp_cb: Idle Timeout\n");
            break;
        }
        case PPPERR_CONNECTTIME:
        {
            printf("ppp_cb: Max connect time reached\n");
            break;
        }
        case PPPERR_LOOPBACK:
        {
            printf("ppp_cb: Loopback detected\n");
            break;
        }
        default:
        {
            printf("ppp_cb: Unknown error code %d\n", err_code);
            break;
        }
    }


    /*
     * This should be in the switch case, this is put outside of the switch
     * case for example readability.
     */

    if (err_code == PPPERR_NONE)
    {
        return;
    }

    /* ppp_close() was previously called, don't reconnect */
    if (err_code == PPPERR_USER)
    {
        /* ppp_free(); -- can be called here */
        return;
    }

    printf("ppp reconnecting...\r\n");
    /*
     * Try to reconnect in 30 seconds, if you need a modem chatscript you have
     * to do a much better signaling here ;-)
     */
    ppp_connect(pcb, 30);
}




static int modem_probe(struct usb_interface *intf, const struct usb_device_id *id)
{
    struct usb_host_interface *iface_desc;
    struct usb_endpoint_descriptor *endpoint;
    int i,ret;
    u8 bulk_in_endpointAddress = 0;
    u8 bulk_out_endpointAddress = 0;
    struct modem *modem;

    //only accept the bInterfaceNumber 0, it is same as ttyUSB0 in linux
    if(intf->cur_altsetting->desc.bInterfaceNumber != 0)
        return -1;


    modem = modem_struct_get();
    if(!modem)
        return -1;


    /* descriptor matches, let's find the endpoints needed */
    /* check out the endpoints */
    iface_desc = intf->cur_altsetting;
    for (i = 0; i < iface_desc->desc.bNumEndpoints; ++i)
    {
        endpoint = &iface_desc->endpoint[i].desc;

        if (!bulk_in_endpointAddress &&
            usb_endpoint_is_bulk_in(endpoint))
        {
            /* we found a bulk in endpoint */
            printf("modem_probe:found bulk in on endpoint %d\r\n", i);
            bulk_in_endpointAddress = endpoint->bEndpointAddress;
        }

        if (!bulk_out_endpointAddress &&
            usb_endpoint_is_bulk_out(endpoint))
        {
            /* we found a bulk out endpoint */
            printf("modem_probe:found bulk out on endpoint %d\r\n", i);
            bulk_out_endpointAddress = endpoint->bEndpointAddress;
        }
    }


    if(!bulk_in_endpointAddress || !bulk_out_endpointAddress)
    {
        printf("modem_probe: error:no bulk out or bulk in endpoint found\r\n");
        modem_struct_put(modem);
        return -2;
    }


    modem->bulk_in_urb = usb_alloc_urb(0, GFP_KERNEL);     /* No ISO */
    if (modem->bulk_in_urb == NULL)
    {
        printf("modem_probe: usb_alloc_urb failed.\r\n");
        modem_struct_put(modem);
        return -3;
    }


    modem->interface = intf;
    modem->bulk_in_endpointAddress = bulk_in_endpointAddress;
    modem->bulk_out_endpointAddress = bulk_out_endpointAddress;
    usb_set_intfdata(intf, modem);

        
#if DEBUG_ATCMD
{
    static unsigned char rcv_buf[256];

    BSP_Ser_Rx_Disable();

    //for debug AT command
    while(1)
    {
        memset(rcv_buf, 0, sizeof(rcv_buf));
        if((ret=modem_read(modem, (u8_t *)rcv_buf, sizeof(rcv_buf), 10))>0){
            BSP_Ser_Write(rcv_buf,ret); 
        }  
        if((ret=BSP_Ser_Read(rcv_buf,sizeof(rcv_buf)))>0){
            modem_write(modem, rcv_buf, ret);
        }
    }
}
#endif


    for(i=0;i<4;i++)
    {
        msleep(3000+2000*i);
        if((ret = modem_dial_china_unicom(modem)) == 0) break;    
//        if((ret = modem_dial_china_telecom(modem)) == 0) break;      
//        if((ret = modem_dial_china_mobile(modem)) == 0) break;      
    }
    if(i==4) goto failed; 


    /* Fill URB using supplied data. */
    usb_fill_bulk_urb(modem->bulk_in_urb, modem->interface->usb_dev,
                      usb_rcvbulkpipe(modem->interface->usb_dev, modem->bulk_in_endpointAddress),
                      modem->bulk_in_buf, MODEM_RX_BUFSIZE, modem_indat_callback, modem);


    ret = usb_submit_urb(modem->bulk_in_urb, GFP_ATOMIC);
    if (ret)
    {
        printf("%s: submit urb failed:%d\r\n", __func__, ret);
        goto failed;
    }

    modem->is_active = 1;

    printf("ppp connecting...\r\n");
    pppapi_connect(modem->ppp, 0);


    return 0;
failed:
    printf("modem_probe: failed %d.\r\n",ret);
    usb_free_urb(modem->bulk_in_urb);
    modem_struct_put(modem);
    return ret;
}




void modem_disconnect(struct usb_interface *intf)
{
    OS_ERR err;
    struct modem *modem;

    modem = usb_get_intfdata(intf);

    OSMutexPend(&modem->lock, 0, OS_OPT_PEND_BLOCKING, NULL, &err);

    if(modem->bulk_in_urb)
    {
        usb_kill_urb(modem->bulk_in_urb);
        usb_free_urb(modem->bulk_in_urb);
    }

    modem->interface = NULL;
    modem->bulk_in_urb = NULL;
    modem->bulk_in_endpointAddress = 0;
    modem->bulk_out_endpointAddress = 0;
    modem->is_active = 0;

    pppapi_close(modem->ppp, 1);

    OSMutexPost(&modem->lock, OS_OPT_POST_1, &err);



    modem_struct_put(modem);
}




struct usb_driver modem_driver =
{
.name       = "modem_driver",
.probe      = modem_probe,
.disconnect = modem_disconnect,
.id_table   = modem_ids,
};


int modem_init(void)
{
    OS_ERR err;
    int i;

    printf("modem_init  --->\n");


    memset(modem_struct, 0, sizeof(modem_struct));


    for (i = 0 ; i < MODEM_MAX; i++ )
    {
        /*
         * Create a new PPPoS interface
         *
         * ppp_netif, netif to use for this PPP link, i.e. PPP IP interface
         * output_cb, PPPoS serial output callback
         * status_cb, PPP status callback, called on PPP status change (up, down, ...)
         * ctx_cb, optional user-provided callback context pointer
         */
        modem_struct[i].ppp = pppapi_pppos_create(&modem_struct[i].pppif,
                                           pppos_output_cb, pppos_status_cb, &modem_struct[i]);
        
        if (!modem_struct[i].ppp)
        {
            printf("modem_init:pppos_create:%d failed!\r\n",i);
            return -1;
        }

        OSMutexCreate(&modem_struct[i].lock, "modem lock", &err);
        OSSemCreate(&modem_struct[i].bulk_out_limit, "modem bulk out limit", 1, &err);
    }


    OSTaskCreate((OS_TCB       *)&ModemTaskTCB,              /* Create the start task                                */
                 (CPU_CHAR     *)"Modem Task",
                 (OS_TASK_PTR   )ModemTask,
                 (void         *)0u,
                 (OS_PRIO       )MODEM_TASK_PRIO,
                 (CPU_STK      *)&ModemTaskStk[0u],
                 (CPU_STK_SIZE  )MODEM_TASK_STK_SIZE / 10u,
                 (CPU_STK_SIZE  )MODEM_TASK_STK_SIZE,
                 (OS_MSG_QTY    )5u,
                 (OS_TICK       )0u,
                 (void         *)0u,
                 (OS_OPT        )(OS_OPT_TASK_STK_CHK | OS_OPT_TASK_STK_CLR),
                 (OS_ERR       *)&err);


    if(err != OS_ERR_NONE)
    {
        printf("modem_init:OSTaskCreate failed:%d\r\n",err);
        return -2;
    }

    return usb_register(&modem_driver);
}

