{
    char *send;
    char *expect;
};


struct modem *modem_struct_get()
{
    int i;

    for ( i = 0 ; i < MODEM_MAX ; i++ )
    {
        if(modem_struct[i].is_used == 0)
        {
            modem_struct[i].is_used = 1;
            return &modem_struct[i];
        }
    }

    return NULL;
}

void modem_struct_put(struct modem *modem)
{
    modem->is_used = 0;
}



/*modem input data callback function*/
static void modem_indat_callback(struct urb *urb)
{
    OS_ERR err;
    struct modem *modem = (struct modem *)urb->context;

//    printf("modem_indat_callback actual_length:%d\r\n",urb->actual_length);

    //signal to ModemTask()
    OSTaskQPost(&ModemTaskTCB, modem, urb->actual_length, OS_OPT_POST_FIFO, &err);

    return;
}

/*modem output data callback function*/
static void modem_outdat_callback(struct urb *urb)
{
    struct modem *modem;
    OS_ERR err;

    modem = urb->context;

    /* sync/async unlink faults aren't errors */
    if (urb->status)
    {
        printf("%s - nonzero write bulk status received: %d\r\n",
               __func__, urb->status);
    }

    OSSemPost(&modem->bulk_out_limit, OS_OPT_POST_1, &err);
}

int modem_read(struct modem *modem, u8_t *data, u32_t len, int timeout)
{
    int ret;
    int actual_length = 0;
    OS_ERR err;


    OSMutexPend(&modem->lock, 0, OS_OPT_PEND_BLOCKING, NULL, &err);

    if(!modem->interface)
    {
        ret = -ENODEV;
        goto RET;
    }

    ret = usb_bulk_msg(modem->interface->usb_dev, usb_rcvbulkpipe(modem->interface->usb_dev, modem->bulk_in_endpointAddress), data, len, &actual_length, timeout);
    if(!ret)
    {
        ret = actual_length;
    }

RET:
    OSMutexPost(&modem->lock, OS_OPT_POST_1, &err);
    return ret;
}


int modem_write(struct modem *modem, u8_t *data, u32_t len)
{
    int retval;
    OS_ERR err;
    struct urb *urb;

//    printf("--->modem_write task:%s\r\n", OSTCBCurPtr->NamePtr);

    //wait last packet output
    OSSemPend(&modem->bulk_out_limit, 0, OS_OPT_PEND_BLOCKING, NULL, &err);
    if(err != OS_ERR_NONE)
    {
        printf("modem_write OSSemPend bulk_out_limit failed:%d\r\n",err);
    }


    OSMutexPend(&modem->lock, 0, OS_OPT_PEND_BLOCKING, NULL, &err);
    //check modem connected
    if(!modem->interface) 
    {
        OSMutexPost(&modem->lock, OS_OPT_POST_1, &err);
        retval = -ENODEV;
        goto err;
    }

    /* create a urb, and a buffer for it, and copy the data to the urb */
    urb = usb_alloc_urb(0, GFP_KERNEL);
    if (!urb)
    {
        retval = -ENOMEM;
        goto err;
    }

    /* initialize the urb properly */
    usb_fill_bulk_urb(urb, modem->interface->usb_dev,
                      usb_sndbulkpipe(modem->interface->usb_dev, modem->bulk_out_endpointAddress),
                      data, len, modem_outdat_callback, modem);


    /* send the data out the bulk port */
    retval = usb_submit_urb(urb, GFP_KERNEL);
    OSMutexPost(&modem->lock, OS_OPT_POST_1, &err);

    /* release our reference to this urb, the USB core will eventually free it entirely */
    usb_free_urb(urb);
    if (retval)
    {
        printf("%s - failed submitting write urb, error %d\r\n", __func__, retval);
        goto err;
    }


    return len;
err:
    OSSemPost(&modem->bulk_out_limit, OS_OPT_POST_1, &err);
//    printf("<----modem_write\r\n");
    return retval;
}



